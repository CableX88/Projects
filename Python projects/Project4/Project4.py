import matplotlib.pyplot as plt
import csv
readin_data = [] ##list to take data read in from CSV file
mod_data = [] #list to hold modified data to be output inside of text file
print('Filename:','ocean_temp.csv')

file_input = input('Filename: ')
with open(file_input,'r') as csv_file:
    csv_reader = csv.reader(csv_file)

    for line in csv_reader:
        temp_data = line[0]
        try:
            if temp_data.isdigit(): ## string method .isdigit() will only check if the string is numeric as an integer
                pass ##uses this to ignore integer data
            else:
                readin_data.append(temp_data)
            
        except ValueError:
            print("Error: bad data")
            
    print('input data:',readin_data)


           
output_file =open('P4out.txt','w')
for md in readin_data:
    new_data = float(md)*100
    mod_data.append(new_data)
print('Output data:',mod_data)
output_data = str(mod_data)

output_file.write(output_data)
output_file.close()

years = range(1852,2012)
plt.plot(years, mod_data)
plt.show()
        
